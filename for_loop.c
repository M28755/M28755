#include <stdio.h>

int main()
{   // variable
	int i;
	
	//   start  ; condition ; step
	for(i = 100 ; i >= 50 ; i--)
	{
		
		printf("\n %d", i);
		
	}printf("\n\n");
	
	return 0;
}